
public class ForLoopChallenge {
public static void main(String[] args) {
	
	for(int classchant=2; classchant <= 10; classchant= classchant+2){
		
		System.out.println(classchant );
		
	}
	
	
	for(int highnum = 99; highnum >= 94; highnum= highnum-1){
		
		System.out.println(highnum);
		
	}
	
	
	
	
	
	
	
	
}
}
